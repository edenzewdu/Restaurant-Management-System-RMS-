         <h2>What's Injera</h2> 
          <img src="../images/injera1.jpg"> 
         <p>  
         Injera is a sour fermented flatbread with a slightly spongy texture, traditionally made of teff flour. In Ethiopia and Eritrea. Injera is central to the dining process, like bread or rice elsewhere.</p><br>

         <p>Traditionally, injera is made with just two ingredients – Eragrostis tef, also known as teff, an ancient grain from the highlands of Ethiopia, which are ground into flour, and water. Teff production is limited to certain middle elevations with adequate rainfall, and, as it is a low-yield crop,it is relatively expensive for the average farming household. </p><br>
         <p>
          As many farmers in the Ethiopian highlands grow their own subsistence grains, wheat, barley, corn, or rice flour are sometimes used to replace some or all of the teff content. Teff seeds are graded according to color, used to make different kinds of injera: nech (white), key or quey (red), and sergegna (mixed).</p><br>
          <p> When teff is not available, usually because of location or financial limitations, injera is made by fermenting a variety of different grains, including barley, millet, and sorghum. Teff is, however, the preferred grain for making injera, primarily because of its sensory attributes (color, smell, taste).[8] Teff flour is gluten-free.</p><br>

<p>There is little written or known about teff's origin and while there is no scholarly consensus, some believe that the production of teff dates back as far as 4000 BC.
